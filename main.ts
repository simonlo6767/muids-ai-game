function showResult(text: string) {
    let timePlayed = (game.runtime() - startTime) / 1000

    game.showLongText(
        text + "\n\n" +
        "\nTime: " + Math.round(timePlayed) + "s" +
        "\nGhosts Used: " + ghostUsed +
        "\nCoins: " + coins,
        DialogLayout.Center
    )

    game.reset()
}
