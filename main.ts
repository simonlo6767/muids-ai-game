game.onUpdate(function () {

    if (ghosts.length < 2) {
        spawnGhost(player.x + randint(60, 120), randint(40, 100))
    }

   

    for (let p of platforms) {
        if (player.vy >= 0 &&
            player.right > p.left &&
            player.left < p.right &&
            player.bottom >= p.top &&
            player.bottom <= p.top + 6) {

            player.vy = 0
            player.bottom = p.top
            jumpAvailable = true
        }
    }

    for (let g of ghosts) {
        if (player.vy >= 0 &&
            player.right > g.left &&
            player.left < g.right &&
            player.bottom >= g.top &&
            player.bottom <= g.top + 6) {

            player.vy = 0
            player.bottom = g.top
            jumpAvailable = true
            
            // ⭐ count usage
            if (!(g.data && g.data["used"])) {
                g.data = g.data || {}
                g.data["used"] = true
                ghostUsed += 1
            }
        }
    }

    let dx = Math.cos(aimAngle * Math.PI / 180) * 50
    let dy = Math.sin(aimAngle * Math.PI / 180) * 50
    aimLine.setPosition(player.x + dx, player.y + dy)
})

